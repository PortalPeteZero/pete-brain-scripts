def splice_autogen(existing, new_autogen):
    """Replace content between AUTOGEN markers; preserve everything else.
    If markers absent, append a new autogen block at the end."""
    if AUTOGEN_START in existing and AUTOGEN_END in existing:
        before = existing.split(AUTOGEN_START)[0]
        after = existing.split(AUTOGEN_END, 1)[1]
        return f"{before}{AUTOGEN_START}\n{new_autogen}\n{AUTOGEN_END}{after}"
    # Fallback: append at end
    return existing + f"\n\n{AUTOGEN_START}\n{new_autogen}\n{AUTOGEN_END}\n"


def bump_frontmatter_updated(text, today):
    """Set the YAML frontmatter `updated:` field to `today`. Operates only on the
    leading `---`..`---` block so body text starting with `updated:` is never touched.
    If frontmatter exists without an `updated:` line, one is appended to it."""
    m = re.match(r"(?s)^(---\n.*?\n)(---\n)", text)
    if not m:
        return text
    fm, close = m.group(1), m.group(2)
    if re.search(r"(?m)^updated:.*$", fm):
        fm = re.sub(r"(?m)^updated:.*$", f"updated: {today}", fm, count=1)
    else:
        fm = fm + f"updated: {today}\n"
    return fm + close + text[m.end():]


def _structural(state):
    """Normalised structural slices for change-detection. Excludes volatile
    metrics / bids / quality scores (they drift every run)."""
    if not state:
        return {}
    g = lambda k: state.get(k, []) or []
    return {
        "campaigns": sorted(
            f"{c.get('name')}|{c.get('status')}|{c.get('channel')}|{c.get('bidding')}|{c.get('budget_micros')}|{c.get('target_cpa_micros')}"
            for c in g("campaigns")),
        "ad_groups": sorted(
            f"{a.get('campaign_name')}|{a.get('name')}|{a.get('status')}|{a.get('type')}"
            for a in g("ad_groups")),
        "keywords": sorted(
            f"{k.get('ad_group_name')}|{k.get('text')}|{k.get('match_type')}|{k.get('status')}"
            for k in g("keywords")),
        "ads": sorted(
            f"{a.get('ad_group_name')}|{a.get('status')}|{','.join(a.get('final_urls') or [])}|{' / '.join(a.get('headlines') or [])}|{' / '.join(a.get('descriptions') or [])}"
            for a in g("ads")),
        "sitelinks": sorted(
            f"{s.get('campaign_name')}|{s.get('link_text')}|{s.get('final_url')}|{s.get('description1')}|{s.get('description2')}"
            for s in g("sitelinks")),
        "other_extensions": sorted(
            f"{e.get('field_type')}|{e.get('asset_type')}" for e in g("other_extensions")),
        "account_extensions": sorted(
            f"{e.get('field_type')}|{e.get('asset_type')}" for e in g("account_extensions")),
        "campaign_negatives": sorted(
            f"{n.get('campaign_name')}|{n.get('text')}|{n.get('match_type')}" for n in g("campaign_negatives")),
        "shared_criteria": sorted(
            f"{s.get('shared_set_name')}|{s.get('text')}|{s.get('match_type')}" for s in g("shared_criteria")),
        "conversion_actions": sorted(
            f"{c.get('name')}|{c.get('status')}|{c.get('primary_for_goal')}|{c.get('category')}" for c in g("conversion_actions")),
    }


def _diff_summary(prev_s, new_s):
    """Return (changed: bool, summary: str) comparing two _structural() dicts."""
    if not prev_s:
        return True, "first snapshot (no prior state to compare)"
    parts = []
    for key in new_s:
        old, new = set(prev_s.get(key, [])), set(new_s.get(key, []))
        if old == new:
            continue
        added, removed = new - old, old - new
        if key == "sitelinks":
            def _label(items):
                return ", ".join(sorted(f"{x.split('|')[1]}→{x.split('|')[2]}" for x in items)) or "—"
            bits = []
            if added:
                bits.append(f"added [{_label(added)}]")
            if removed:
                bits.append(f"removed [{_label(removed)}]")
            parts.append("sitelinks " + "; ".join(bits))
        else:
            counts = []
            if added:
                counts.append(f"+{len(added)}")
            if removed:
                counts.append(f"-{len(removed)}")
            parts.append(f"{key.replace('_', ' ')} {'/'.join(counts)}")
    if not parts:
        return False, "no structural change"
    return True, "; ".join(parts)


